// SUM OF TWO INTEGERS
#include <stdio.h>

int main() {
    int a, b, result;

    printf("Enter first no.: ");
    scanf("%d", &a);

    printf("Enter second no.: ");
    scanf("%d", &b);

    result = a + b;

    printf("a = %d\n", a);
    printf("b = %d\n", b);
    printf("result = %d\n", result);

    return 0;
}
